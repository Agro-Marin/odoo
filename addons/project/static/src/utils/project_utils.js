/**
 * Persisted "Show Sub-Tasks" toggle, shared between the task control panel
 * and the task model mixin (single source of truth for the storage key and
 * for corrupted-value handling: a non-JSON value must not brick every task
 * view with a SyntaxError on load).
 */
const SHOW_SUBTASKS_STORAGE_KEY = "showSubtasks";

export function getShowSubtasks() {
    try {
        return Boolean(JSON.parse(browser.localStorage.getItem(SHOW_SUBTASKS_STORAGE_KEY)));
    } catch {
        return false;
    }
}

export function setShowSubtasks(value) {
    browser.localStorage.setItem(SHOW_SUBTASKS_STORAGE_KEY, Boolean(value));
}

/**
 * Focus the record's title input once the form is in the DOM (used by the
 * project and task form controllers when opened with the focusTitle prop,
 * e.g. right after creating a record from a template).
 *
 * @param {{ el: HTMLElement | null }} rootRef the controller's root ref
 */
export function useFocusTitle(rootRef) {
    useEffect(
        (el) => {
            // #name_0 is the autogenerated id of the first `name` field input.
            el?.querySelector("#name_0")?.focus();
        },
        () => [rootRef.el]
    );
}
